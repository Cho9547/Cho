# print 함수
# 괄호 안의 값을 console에 출력해주는 함수이다.
# 하나의 print()이 실행되면 자동으로 개행처리가 된다.
print("1", end='')
print("2")

# end 옵션
# 기본은 \n으로 설정되어 있고, end= 에 새로운 설정을 할 수 있음.
# 문장의 마지막을 지정할 수 있는 매개변수.

# sep 옵션
# 띄어쓰기 형식을 지정.
print("1", "2", "3", "4", sep = ",")


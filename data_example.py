# 자료 (Data)
# 실질적인 값을 의미. 숫자, 문자 등 실질적으로 표현되는 값을 Data라고 한다.

# 자료형 (Data type)
# 실질적인 값의 형태를 의미.
# 대표적으로 숫자형, 문자형, 논리형(불린형 boolean)

# 숫자형(numeric)
# 숫자형 값 표기방법
#   1. 정수형
print(1)
print(-1)

#   2. 실수형
print(3.14)
print(-3.14)

# 문자형(character/String)
print("Hello") # 문자열 ""
print('Hello') # 문자 '' (파이썬 예외)

print("오늘은 파이썬을 시작하는 '첫 날' 입니다!")

print('"이름"')

# 숫자형과 문자형의 차이 인지하기
print(1 + 1)
print("1" + "1")

# 논리형(불린형)
# 참 또는 거짓을 의미하는 데이터 형식이다.
# 단순해 보이지만 이후 배울 판별식, 분기문(조건문) 등에 자주 사용되는 데이터 타입.
print(True)
print(False)
